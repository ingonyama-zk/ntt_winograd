#define N 32

int reference_ntt(ap_uint<254> matr[N][N], ap_uint<254> x[N], ap_uint<254> y[N]) {
	int i,j;
	ap_uint<1024> yi, product_red;
	for(i=0;i<N;i++) {
		yi=ap_uint<1024>("0");
		for(j=0;j<N;j++) {
            product_red = matr[i][j]*x[j] % PMOD;
            yi += product_red;
			yi = yi % PMOD;
		}
		y[i] = yi;
	}
	return 0;
}

